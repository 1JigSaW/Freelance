G = 6.7 * (10**(-8))

mv = 4.86 * pow(10, 27)
mz = 5.98 * pow(10, 27)
ms = 5.68 * pow(10, 29)

rv = 6175
rz = 6371
rs = 57750

g_v = G*(mv / (rv**2))
g_z = G*(mz / (rz**2))
g_s = G*(ms / (rs**2))

if (g_v > g_z) and (g_v > g_s):
	print('Наибольшее ускорение силы тяжести имеет планета - Венера')
elif (g_z > g_v) and (g_z > g_s):
	print('Наибольшее ускорение силы тяжести имеет планета - Земля')
else:
	print('Наибольшее ускорение силы тяжести имеет планета - Сатурн')