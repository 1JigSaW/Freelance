# Определить количество счастливых билетов в рулоне с номерами билетов от 000001 до 999999.

summa = 0
for i in range(1, 1000000):
	num1 = i//100000
	num2 = i//10000%10
	num3 = i//1000%10%10
	num4 = i//100%10%10%10
	num5 = i//10%10%10%10%10
	num6 = i%10%10%10%10
	if num1 + num2 + num3 == num4 + num5 + num6:
		summa += 1
	
print(summa)